# If a runner runs 10 miles in 30 minutes and 30 seconds,
# What is their average speed in kilometers per hour?
# (Tip: 1 mile = 1.6 km)

speed_milesperminutes = 10 / 1830   # miles       3600   seconds     1.6    km
                                    # seconds       1  hour        1      miles
speed_kilometersperhour = speed_milesperminutes *3600*1.6

#velocidad (m/s) = distancia (m)/tiempo(s)

print("10 miles in 30 minutes and 30 seconds are equivalent to: ", speed_kilometersperhour)
